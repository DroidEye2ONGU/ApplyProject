public class AdjuBean {

    private String adjuID;
    private String adjuPassword;
    private String adjuName;

    public AdjuBean() {
    }

    public String getAdjuID() {
        return adjuID;
    }

    public void setAdjuID(String adjuID) {
        this.adjuID = adjuID;
    }

    public String getAdjuPassword() {
        return adjuPassword;
    }

    public void setAdjuPassword(String adjuPassword) {
        this.adjuPassword = adjuPassword;
    }

    public String getAdjuName() {
        return adjuName;
    }

    public void setAdjuName(String adjuName) {
        this.adjuName = adjuName;
    }
}
